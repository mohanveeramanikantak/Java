// find flowers in if-else-if
class Flowers_find
{
	public static void main(String args[])
	{
		int flower=3;
		System.out.println("1=lilly");
		System.out.println("2=rose");
		System.out.println("3=jasmine");
		System.out.println("enther the flowers");
		if(flower==1)
			System.out.println("lilly");
		else if(flower==2)
			System.out.println("rose");
		else if(flower==3)
			System.out.println("jasmine");
		else
			System.out.println("not mentioned");
	}
}

